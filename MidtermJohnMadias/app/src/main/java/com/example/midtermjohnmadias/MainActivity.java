    public void conversion(View view) {
        EditText amount =(EditText) findViewById(R.id.milesAmount);
        Double milesAmount = Double.parseDouble(amount.getText().toString());
        Double kmsAmount= milesAmount * 1.609;
        Toast.makeText(getApplicationContext(),"Miles to Kms conversion is: "+ kmsAmount.toString(), Toast.LENGTH_LONG).show();
    }

